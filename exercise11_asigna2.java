public class exercise11_asigna2 { //se declara la clase principal 
 
   private static final double radius = 7.5; //se declara el radio como dato privado 

    public static void main(String[] args) { //se define la funcion principal con el instanciador main 

        double perimeter = 2 * Math.PI * radius; //se realiza la asignacion por medio 
        double area = Math.PI * radius * radius; //de =

        System.out.println("Perimeter is = " + perimeter); // se imprime en pantalla a travez de system.out el perimeter 
        System.out.println("Area is = " + area); // se imprime en pantalla a el area que se habia asignado anteriormente 
    }
}
